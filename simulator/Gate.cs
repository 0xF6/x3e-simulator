namespace Simulator
{
    using System.IO;
    using Microsoft.AspNetCore.Hosting;

    internal class Gate
    {
        public static void Main(string[] args)
        {
            var host = new WebHostBuilder()
                .UseContentRoot(Directory.GetCurrentDirectory())
                .UseKestrel()
                .UseStartup<Startup>()
                .Build();

            host.Run();
        }
    }
}
